package edu.hist.team3.catering.route;

public class TextValue {
	private String text;
	private double value;
	
	public TextValue() {
		
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

	public double getValue() {
		return value;
	}

	public void setValue(double value) {
		this.value = value;
	}
}